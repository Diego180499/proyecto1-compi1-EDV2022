package python;

/**
 *
 * @author HP
 */
public class Declaracion {
    
    private String id;
    private String value;

    public Declaracion(String id, String value) {
        this.id = id;
        this.value = value;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "var "+id+" = "+value;
    }
    
}
